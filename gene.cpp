#include <stdlib.h>

#include "gene.h"

using namespace std;

Gene::Gene() :
	coord(INVALID_COORD)
{
}

//*************************************************************************************************************
//*************************************************************************************************************

Gene::~Gene() {
}

//*************************************************************************************************************
//*************************************************************************************************************

void Gene::setRandom() {
	coord = MIN_COORD + static_cast<float>(rand()) / (static_cast<float>(RAND_MAX / (MAX_COORD - MIN_COORD)));
}

//*************************************************************************************************************
//*************************************************************************************************************

void Gene::printToFile(ofstream& outputFile) const {
	outputFile << coord;
}
